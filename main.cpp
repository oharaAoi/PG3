#include <stdio.h>
#include <cstdio>

//////////////////////////////////////////////////////////////////////////////////////////////////
// ↓　関数群
//////////////////////////////////////////////////////////////////////////////////////////////////

template <typename T>
T Min(T a, T b) {
	if (a < b) {
		return a;
	} else {
		return b;
	}
}

template <>
const char* Min<const char*>(const char* a, const char* b) {
	return "数字以外の代入は出来ません";
}

//////////////////////////////////////////////////////////////////////////////////////////////////
// ↓　main処理
//////////////////////////////////////////////////////////////////////////////////////////////////

int main() {
	printf("1 : 3 = %d\n", Min(1, 3));
	printf("1.0f : 3.0f = %f\n", Min(1.0f, 3.0f));
	printf("1.0 : 3.0 = %lf\n", Min(1.0, 3.0));
	printf("1.0 : 3.0 = %s\n", Min("1", "3"));
	return 0;
}